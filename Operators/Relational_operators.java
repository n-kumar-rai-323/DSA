public class Relational_operators {
    public static void main(String[] args) {
        // Relational operators 
        // == equal to
        // != not equal to
        // > greater
        // < less than
        // >= greater than equal
        // <= less than equal
        int a = 10;
        int b = 5;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
